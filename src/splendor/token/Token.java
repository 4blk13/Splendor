package splendor.token;

/**
 *  Colors of tokens can be used.
 *  {@link #WHITE}
 *  {@link #BLUE}
 *  {@link #GREEN}
 *  {@link #RED} 
 *  {@link #BLACK}
 *  {@link #GOLD}
 */
public enum Token {
	/**
     * White color
     */
	WHITE,
	
	/**
     * Blue color
     */
	BLUE,
	/**
     * Green color
     */
	GREEN,
	/**
     * Red color
     */
	RED,
	/**
     * Black color
     */
	BLACK,
	/**
     * Gold color
     */
	GOLD;
}

